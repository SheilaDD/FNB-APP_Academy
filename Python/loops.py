#Loops

fruits = ['apple', 'banana', 'cherry', 'kiwi', 'mango']

for fruit in fruits:
    print(fruit)
# Using a for loop to iterate through a list of fruits
# Output: apple, banana, cherry, kiwi, mango  

numbers = [1, 2, 3, 4, 5]
for number in numbers:
    print(number)


#while loop

count = 0
# Using a while loop to print numbers from 0 to 4
# The loop continues until count is less than 10
while count < 10:
    print("Count is:", count)
    count += 1 # Incrementing count by 1 in each iteration
# Output: Count is: 1, Count is: 2, Count is: 3, Count is: 4

